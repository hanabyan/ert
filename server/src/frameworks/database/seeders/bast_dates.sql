-- Verifikasi hasil
SELECT 
    block, 
    number, 
    bast_date, 
    DATE_FORMAT(bast_date, '%d/%m/%Y') as formatted_date,
    CASE 
        WHEN bast_date IS NULL THEN 'Belum diset'
        ELSE 'Sudah diset'
    END as status
FROM properties 
ORDER BY block, CAST(number AS UNSIGNED);

-- Hitung total properti dengan BAST per blok
SELECT 
    block,
    COUNT(*) as total_properties,
    SUM(CASE WHEN bast_date IS NOT NULL THEN 1 ELSE 0 END) as with_bast,
    SUM(CASE WHEN bast_date IS NULL THEN 1 ELSE 0 END) as without_bast
FROM properties
GROUP BY block
ORDER BY block;

-- Total keseluruhan
